    /**
     * 加锁
     *
     * @param key
     * @param value 当前时间+超时时间
     * @return
     */
    public boolean lock(String key, String value) {
        //如果key不存在返回true,并设置，如果存在返回false
        Boolean aBoolean = stringRedisTemplate.opsForValue().setIfAbsent(key, value);
        if (aBoolean) {
            return aBoolean;
        }
        //key已经有值了,获得value
        String currentValue = stringRedisTemplate.opsForValue().get(key);
        //当前时间大于锁的时间，说明已经过期
        if (!StringUtils.isEmpty(currentValue) && Long.parseLong(currentValue) < System.currentTimeMillis()) {
            //获取上一个锁的值
            String oldValue = stringRedisTemplate.opsForValue().getAndSet(key, value);
            //第一个获得锁的线程
            if (!StringUtils.isEmpty(oldValue) && oldValue.equals(currentValue)) {
                return true;
            }
        }
        return false;
    }

    /**
     * 解锁
     *
     * @return
     */
    public Boolean unlock(String key, String value) {
        //如果key存在，那就设置value,并获得之前的value,如果key不存在设置value.返回
        String currValue = stringRedisTemplate.opsForValue().get(key);
        if (!StringUtils.isEmpty(currValue) && currValue.equals(value)) {
            stringRedisTemplate.delete(key);
            return true;
        }
        return false;
    }


